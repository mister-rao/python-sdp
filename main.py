# write a function calculate 2nd power of a number 
# return the result from the function and print the result
# using ** (exponent operator)
def power_2(a: int):
    return a ** 2

print(power_2(6))

# write function to calculate a ** n
def power_n(a: int, n: int):
    return a ** n

print(power_n(2, 4))